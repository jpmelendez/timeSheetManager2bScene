package com.twobScene.web.dto;

public class ConsultantDTO {

	
	private Long consultantID;
	private String consultantCategory;
	private String companyName;
	private String contactName;
	private String contactEmail;
	private String phone;
	private String mobile;
	private String fax;
	private String address;
	private String consultantStatus;
	public Long getConsultantID() {
		return consultantID;
	}
	public void setConsultantID(Long consultantID) {
		this.consultantID = consultantID;
	}
	public String getConsultantCategory() {
		return consultantCategory;
	}
	public void setConsultantCategory(String consultantCategory) {
		this.consultantCategory = consultantCategory;
	}
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getContactName() {
		return contactName;
	}
	public void setContactName(String contactName) {
		this.contactName = contactName;
	}
	public String getContactEmail() {
		return contactEmail;
	}
	public void setContactEmail(String contactEmail) {
		this.contactEmail = contactEmail;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getConsultantStatus() {
		return consultantStatus;
	}
	public void setConsultantStatus(String consultantStatus) {
		this.consultantStatus = consultantStatus;
	}
	
	
}

